'''
### Problem 1.9 ⭐⭐ (Easy)
Write a program that stores the temperature in Celsius and prints it in both Celsius and Fahrenheit.

**Formula:** F = (C × 9/5) + 32

**Example:** If C = 0, print:
```
0°C is 32°F
```

'''
celsius = 0
fahrenheit = (celsius * 9/5) + 32
print(f"{celsius}°C is {fahrenheit}°F")